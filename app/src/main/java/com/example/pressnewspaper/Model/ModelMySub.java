package com.example.pressnewspaper.Model;

public class ModelMySub {
    private String newsPaperId;
    private String newsPaperName;
    private String subscriptionsType;
    private String subscriptionsStartDate;
    private String subscriptionsEndDate;

    public String getNewsPaperId() {
        return newsPaperId;
    }

    public void setNewsPaperId(String newsPaperId) {
        this.newsPaperId = newsPaperId;
    }

    public String getNewsPaperName() {
        return newsPaperName;
    }

    public void setNewsPaperName(String newsPaperName) {
        this.newsPaperName = newsPaperName;
    }

    public String getSubscriptionsType() {
        return subscriptionsType;
    }

    public void setSubscriptionsType(String subscriptionsType) {
        this.subscriptionsType = subscriptionsType;
    }

    public String getSubscriptionsStartDate() {
        return subscriptionsStartDate;
    }

    public void setSubscriptionsStartDate(String subscriptionsStartDate) {
        this.subscriptionsStartDate = subscriptionsStartDate;
    }

    public String getSubscriptionsEndDate() {
        return subscriptionsEndDate;
    }

    public void setSubscriptionsEndDate(String subscriptionsEndDate) {
        this.subscriptionsEndDate = subscriptionsEndDate;
    }
}
